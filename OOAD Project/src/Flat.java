
public class Flat {
	private String FlatId, OwnerName, Description, FlatLocation, IntercomeNumber,RentCost,FamilyId;

	public void setFlatId(String flatId) {
		FlatId = flatId;
	}

	public void setOwnerName(String ownerName) {
		OwnerName = ownerName;
	}

	public void setDescription(String description) {
		Description = description;
	}

	public void setFlatLocation(String flatLocation) {
		FlatLocation = flatLocation;
	}

	public void setIntercomeNumber(String intercomeNumber) {
		IntercomeNumber = intercomeNumber;
	}

	public void setRentCost(String rentCost) {
		RentCost = rentCost;
	}

	public void setFamilyId(String familyId) {
		FamilyId = familyId;
	}
	
	public String getFlatId() {
		return FlatId;
	}

	public String getOwnerName() {
		return OwnerName;
	}

	public String getDescription() {
		return Description;
	}

	public String getFlatLocation() {
		return FlatLocation;
	}

	public String getIntercomeNumber() {
		return IntercomeNumber;
	}

	public String getRentCost() {
		return RentCost;
	}

	public String getFamilyId() {
		return FamilyId;
	}
}